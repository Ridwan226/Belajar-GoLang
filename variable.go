package main

import "fmt"

func main() {
	var name = "Ridwan Romadhon"
	fmt.Println(name)
	
	name = "Ridwan Ganteng"
	fmt.Println(name)
	
	// Menyederhanakan
	
	namesederhana := "Ridwan Romadhon Sederhana"
	fmt.Println(namesederhana)
	
	namesederhana = "Ridwan Ganteng Sederhana"
	fmt.Println(namesederhana)
	
	
	// Variabel secara Tergabung
	
	var (
		firstName = "Ridwan"
		middleName = "Ganteng"
	)
	
	fmt.Println(firstName)
	fmt.Println(middleName)
	
}